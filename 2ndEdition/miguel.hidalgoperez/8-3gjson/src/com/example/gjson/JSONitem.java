package com.example.gjson;

public class JSONitem {
	
	String mDate;
	String minfoUri;
	String mName;
	String mPhotoUri;
	
	public String getmDate() {
		return mDate;
	}
	public void setmDate(String mDate) {
		this.mDate = mDate;
	}
	public String getMinfoUri() {
		return minfoUri;
	}
	public void setMinfoUri(String minfoUri) {
		this.minfoUri = minfoUri;
	}
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getmPhotoUri() {
		return mPhotoUri;
	}
	public void setmPhotoUri(String mPhotoUri) {
		this.mPhotoUri = mPhotoUri;
	}
	
	

}
